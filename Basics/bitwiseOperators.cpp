#include<iostream>
using namespace std;
int main(){
    int a=8,b=5;
    cout<<(a & b)<<endl;//bitwise and operator
    cout<<(a | b)<<endl;//bitwise or operator
    cout<<(a ^ b)<<endl;//bitwise xor operator
    cout<<(4<<1)<<endl;//right shift operator
    cout<<(4>>1)<<endl;//left shift operator
    return 0;
}